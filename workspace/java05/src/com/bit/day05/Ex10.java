class Ex10{

  public static void main(String[] args){
	char ch = '@';

	switch(ch){
		case '0':
			System.out.println("0입니다.");
			break;
		case 'A':
			System.out.println("A입니다.");
			break;
		case '@':
			System.out.println("@입니다.");
			break;
		default:
			System.out.println("이도저도아닙니다.");
			break;
		}


  }

}